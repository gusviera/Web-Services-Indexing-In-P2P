import javax.persistence.*;

/**
 * Entity class of function word
 */
@Entity
public class FunctionWord
{
	/*=========================================================================
	 *					Fields
	 *=========================================================================*/
	
	private String[] words;	// member of class to store in database
	

	/*=========================================================================
	 *					Constructors
	 *=========================================================================*/
	
	public FunctionWord(String[] words)
	{
		this.words=words;
	}
	
	/*=========================================================================
	 *					Getters
	 *=========================================================================*/
	
	/**
	 * @return the word
	 */
	public String[] getWords()
	{
		return words;
	}
	
	/*=========================================================================
	 *					Methods
	 *=========================================================================*/
}
